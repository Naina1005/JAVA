public class Main{
    public static void main(String[] args){
        System.out.println("This program includes a comparison between pre-increment and post-increment");
        int x = 5;
        int y = 8;
        System.out.println("value of x = "+ x);
        System.out.println("value of x++ = "+ x++);
        System.out.println("value of ++x = "+ ++x);
        System.out.println("value of ++x = "+ ++x);
        System.out.println("value of y = "+ y);
        System.out.println("value of y++ = "+ y++);
        System.out.println("value of ++y = "+ ++y);
        System.out.println("value of ++y = "+ ++y);

    }
}