public class Main{
    public static void main(String[] args){
        /*
        Various Primitive types of datatypes are : int, long, byte, short, float, double, char, boolean
         */

        //declaring various variables wrt different Datatypes
        int a = 12;
        long b = 13;
        byte c = 14;
        short d = 15;
        float e = 16;
        double f = 17;
        char g = 'n';
        boolean h = false;

        //Printing output
        System.out.println("Int : "+a);
        System.out.println("Long : "+b);
        System.out.println("Byte : "+c);
        System.out.println("Short : "+d);
        System.out.println("Float : "+e);
        System.out.println("Double : "+f);
        System.out.println("Char : "+g);
        System.out.println("Boolean : "+h);

    }
}